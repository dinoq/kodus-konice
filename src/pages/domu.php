        		<h1>Komunitní dům seniorů</h1>
            	<p>
        			Nabízíme k pronájmu komunitní dům seniorů starších <b>60ti let</b>.
        			Objekt je po celkové rekonstrukci přímo na náměstí v <b>malebném
        			městečku Konice</b>, která se nacház 25 km od Prostějova a 30 km od Olomouce.
        			V komunitním domě se nachází <b>24 nových malometrážních bytů</b>,
        			které jsou vybaveny kuchyňskou linkou s dřezem a elektrickým sporákem. 
        			Všechny byty jsou <b>bezbariérové</b> - vhodné pro osoby se sníženou pohyblivostí.
        			Bezbariérová koupelna je vybavena sprchovým koutem, wc a umyvadlem.
        			
